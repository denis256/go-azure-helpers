var _ resourceids.ResourceId = KustoDatabaseId{}

func TestNewKustoDatabaseID(t *testing.T) {
	id := NewKustoDatabaseID("12345678-1234-9876-4563-123456789012", "example-resource-group", "clusterValue", "databaseValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.KustoClusterName != "clusterValue" {
		t.Fatalf("Expected %q but got %q for Segment 'KustoClusterName'", id.KustoClusterName, "clusterValue")
	}

	if id.KustoDatabaseName != "databaseValue" {
		t.Fatalf("Expected %q but got %q for Segment 'KustoDatabaseName'", id.KustoDatabaseName, "databaseValue")
	}
}

func TestFormatKustoDatabaseID(t *testing.T) {
	actual := NewKustoDatabaseID("12345678-1234-9876-4563-123456789012", "example-resource-group", "clusterValue", "databaseValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Kusto/clusters/clusterValue/databases/databaseValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}


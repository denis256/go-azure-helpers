var _ resourceids.ResourceId = SpringCloudServiceId{}

func TestNewSpringCloudServiceID(t *testing.T) {
	id := NewSpringCloudServiceID("12345678-1234-9876-4563-123456789012", "example-resource-group", "serviceValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.ServiceName != "serviceValue" {
		t.Fatalf("Expected %q but got %q for Segment 'ServiceName'", id.ServiceName, "serviceValue")
	}
}

func TestFormatSpringCloudServiceID(t *testing.T) {
	actual := NewSpringCloudServiceID("12345678-1234-9876-4563-123456789012", "example-resource-group", "serviceValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.AppPlatform/spring/serviceValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

